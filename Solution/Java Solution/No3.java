import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef {
          public static void main(String[] args) {
                    Scanner sc = new Scanner(System.in);
                    int x = sc.nextInt(); // read first integer
                    int y = sc.nextInt(); // read second integer

                    // Compare y with x
                    if (y > x) {
                              System.out.println("YES");
                    } else {
                              System.out.println("NO");
                    }
          }
}